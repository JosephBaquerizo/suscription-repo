package ec.com.zurich.suscription.resources.entity;

import jakarta.persistence.*;
import lombok.Data;
import org.hibernate.annotations.GenericGenerator;

import java.time.LocalDateTime;

@Data
@Entity
@Table(name = "CONCEPTO", schema = "SA")
public class Concepto {

    @Id
    @Column(name = "ID", nullable = false, length = 16)
    @GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "SEC_GEN")
    @GenericGenerator(name = "SEC_GEN", strategy = "ec.com.zurich.suscription.util.CustomSequenceGenerator")
    private String id;

    @Column(name = "DESCRIPCION", nullable = false)
    private String descripcion;

    @Column(name = "ORDEN", nullable = false)
    private Integer orden;

    @Column(name = "GRAVAIVA", length = 1)
    private String gravaIva;

    @Column(name = "TIPO", length = 1)
    private String tipo;

    @Column(name = "USUARIOACTUALIZA", nullable = false)
    private String usuarioActualiza;

    @Column(name = "FECHAACTUALIZA", nullable = false)
    private LocalDateTime fechaActualiza;

    @Column(name = "PAGOSERVICIOS", columnDefinition = "VARCHAR2(1) DEFAULT '0'")
    private String pagoServicios;

    @PrePersist
    public void prePersist() {
        fechaActualiza = LocalDateTime.now();
    }

    @PreUpdate
    public void preUpdate() {
        fechaActualiza = LocalDateTime.now();
    }
}
